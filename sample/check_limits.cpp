#include <bits/stdc++.h>
using namespace std;

int main() {
    std::cout << "short min:" << std::numeric_limits<short>::min() << endl;
    std::cout << "short max:" << std::numeric_limits<short>::max() << endl;
    std::cout << "int min:" << std::numeric_limits<int>::min() << endl;
    std::cout << "int max:" << std::numeric_limits<int>::max() << endl;
    std::cout << "long min:" << std::numeric_limits<long>::min() << endl;
    std::cout << "long max:" << std::numeric_limits<long>::max() << endl;
    std::cout << "ll min:" << std::numeric_limits<long long>::min() << endl;
    std::cout << "ll max:" << std::numeric_limits<long long>::max() << endl;
}